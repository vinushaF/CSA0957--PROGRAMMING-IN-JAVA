import java.util.*;
import java.lang.*;
public class fact{
public static void main(String args[]){
try{
Scanner obj=new Scanner(System.in);
System.out.print("enter the range : ");
int n=obj.nextInt();
if(n<0){
System.out.print("negative numbers cannot exist!!!");
System.exit(0);
}
int f=1;
for(int i=1;i<=n;i++){
f=f*i;
}
System.out.println("factorial of "+ n +" number : "+ f);
}
catch(Exception e){
System.out.println("invalid input!!!");
}
}
}
